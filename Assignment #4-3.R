install.packages(c("tidyr", "devtools"))
install.packages("lawstat")
sparrow <-  c(22.0, 23.9, 20.9, 23.8, 25.0, 24.0, 21.7, 23.8, 22.8, 23.1, 23.1, 23.5, 23.0, 23.0)
Robin <- c(21.8, 23.0, 23.3, 22.4, 23.0, 23.0, 23.0, 22.4, 23.9, 22.3, 22.0, 22.6, 22.0, 22.1, 21.1, 23.0)
Wren <- c(19.8, 22.1, 21.5, 20.9, 22.0, 21.0, 22.3, 21.0, 20.3, 20.9, 22.0, 20.0, 20.8, 21.2, 21.0)

sparrowdf <-  data.frame(group = "sparrow", mm = sparrow)
robindf <-  data.frame(group = "robin", mm = Robin)
wrendf <-  data.frame(group = "wren", mm = Wren)
data <- rbind(sparrowdf, robindf, wrendf)
levene.test(data$mm, data$group, location="mean")
shapiro.test(sparrow)
shapiro.test(Robin)
shapiro.test(Wren)

anova <-  lm(mm ~ group, data = data)
anova(aov)
aov <- aov(mm ~ group, data= data)
summary(anova)